    private void login_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_login_btnActionPerformed
        try{
        if(username_txt_fld.getText().isEmpty() || String.valueOf(password_txt_fld.getPassword()).isEmpty()){
            JOptionPane.showMessageDialog(this, "Korisnicko ime i lozinka moraju biti popunjeni");
            return;
        }
        User u = new User();
        u.setUsername(username_txt_fld.getText());
        u.setPassword(new String(password_txt_fld.getPassword()));
        
        User korisnik = ClientController.getInstance().login(u);
        
        JOptionPane.showMessageDialog(this, "Uspe≈°no ste se prijavili na sistem");
        
        sesija.Session.getInstance().setUlogovani(korisnik);
        System.out.println("I ovaj deo je uspesno zavrsen u login formi");
        
        
        //prikazi formu koja treba sledeca da se otvori
        MeniAplikacije ma = new MeniAplikacije();
        ma.setVisible(true);
        this.dispose();
        
        }
        catch(Exception e){
           
            e.printStackTrace(); 
        }
    }//GEN-LAST:event_login_btnActionPerformed

